    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String endereco = txtEndereco.getText();
        String numero = txtNumero.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();
        String telefone = txtTelefone.getText();

        int selectedRow = tblClientes.getSelectedRow();

        if (selectedRow != -1) {
            modelo.setValueAt(nome, selectedRow, 0);
            modelo.setValueAt(cpf, selectedRow, 1);
            modelo.setValueAt(endereco, selectedRow, 2);
            modelo.setValueAt(numero, selectedRow, 3);
            modelo.setValueAt(cidade, selectedRow, 4);
            modelo.setValueAt(estado, selectedRow, 5);
            modelo.setValueAt(telefone, selectedRow, 6);
        } else {
            modelo.addRow(new Object[]{nome, cpf, endereco, numero, cidade, estado, telefone});
        }

        txtNome.setText("");
        txtCpf.setText("");
        txtEndereco.setText("");
        txtNumero.setText("");
        txtCidade.setText("");
        txtEstado.setText("");
        txtTelefone.setText("");

        if (nome.isEmpty() || cpf.isEmpty() || endereco.isEmpty() || numero.isEmpty()
                || cidade.isEmpty() || estado.isEmpty() || telefone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String dadosCliente = String.format("Nome: %s\nCPF: %s\nEndereço: %s, %s\nCidade: %s\nEstado: %s\nTelefone: %s",
                nome, cpf, endereco, numero, cidade, estado, telefone);
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int selectedRow = tblClientes.getSelectedRow();
        if (selectedRow != -1) {
            String nome = (String) modelo.getValueAt(selectedRow, 0);
            String cpf = (String) modelo.getValueAt(selectedRow, 1);
            String endereco = (String) modelo.getValueAt(selectedRow, 2);
            String numero = (String) modelo.getValueAt(selectedRow, 3);
            String cidade = (String) modelo.getValueAt(selectedRow, 4);
            String estado = (String) modelo.getValueAt(selectedRow, 5);
            String telefone = (String) modelo.getValueAt(selectedRow, 6);

            txtNome.setText(nome);
            txtCpf.setText(cpf);
            txtEndereco.setText(endereco);
            txtNumero.setText(numero);
            txtCidade.setText(cidade);
            txtEstado.setText(estado);
            txtTelefone.setText(telefone);
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int selectedRow = tblClientes.getSelectedRow();
        if (selectedRow != -1) {
            modelo.removeRow(selectedRow);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para excluir.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

